# Good to understand

# | Concept             | Example                        | Output                  |
# | ------------------- | ------------------------------ | ----------------------- |
# | Basic f-string      | `f"Hello {name}"`              | `Hello Juan`            |
# | Thousands separator | `f"{num:,}"`                   | `25,000`                |
# | Decimal places      | `f"{num:.2f}"`                 | `25.00`                 |
# | Combined            | `f"${num:,.2f}"`               | `$25,000.00`            |
# | Left/Right align    | `f"{text:<10}" / f"{num:>10}"` | Align neatly in columns |
